// Uma string herda propriedades e métodos do construtor String()

var nome = 'Lucas'

nome.charAt(1)                      // Saída: 'u' (retorna o caractere na posição especificada.)
nome.replace('cas', 'ke')           // Saída: Luke (substitui uma string por outra e retorna a string com as substituições especificadas.)
nome.length                         // Saída: 5 (retorna a quantidade de caracteres.)
nome                                // Saída: 'Lucas'