// Abreviação de condicionais com if e else.
// Geralmente utilizado quando precisamos atribuir um valor para uma variável, dependendo de uma condição.
var idade = 19
var podeBeber = (idade >= 18) ? 'Pode beber' : 'Não pode beber'     // O parênteses () não é obrigatório!
console.log(podeBeber)  // Saída: Pode beber

